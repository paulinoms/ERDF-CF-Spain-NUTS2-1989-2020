##  ══════════════════════════════════════════════════════════════════════════
##  PART II — MONETARY CONVERSION METHODOLOGY (to € constant 2000)
##  ══════════════════════════════════════════════════════════════════════════
##
##  All monetary values are expressed in thousands of euros at constant 2000
##  prices (m_eur00), regardless of the original currency. Conversion follows
##  two routes depending on the period:
##
##  ROUTE A — Pre-2000 (data in MECUs or M€ linked to ECU):
##  ─────────────────────────────────────────────────────────────────────────
##  Formula:  m€00_t = M_orig_t × EUR/ECU_t × 1000
##
##  Source of EUR/ECU factors:
##    EUROSTAT, table [ert_bil_conv_a] — annual ECU/euro exchange rate
##
##  EUR/ECU factors (verified against the source Excel):
##    ┌──────┬────────────┐
##    │ Year │  EUR/ECU   │
##    ├──────┼────────────┤
##    │ 1989 │ 0.78375500 │
##    │ 1990 │ 0.77777800 │
##    │ 1991 │ 0.77211200 │
##    │ 1992 │ 0.79649500 │
##    │ 1993 │ 0.89625400 │
##    │ 1994 │ 0.95511900 │
##    │ 1995 │ 0.97965000 │
##    │ 1996 │ 0.96611200 │
##    │ 1997 │ 0.99699900 │
##    │ 1998 │ 1.00479700 │
##    │ 1999 │ 1.00000000 │  ← 1 ECU = 1 € (EU Council decision)
##    └──────┴────────────┘
##
##  Verification example (Sheet Reg.89-93, Aragon, Axis 1, 1989):
##    472.973 Mptas × 0.007668 ECU/Pta = 3.627 MECUs
##    3.627 MECUs × 0.783755 EUR/ECU   = 2.843 M€00 × 1000 = 2.843 m€00
##
##  ROUTE B — Post-1999 (data in M€ current prices):
##  ─────────────────────────────────────────────────────────────────────────
##  Formula:  m€00_t = M€_current_t × CPI_factor_cumulative_t × 1000
##
##  Deflator source:
##    INE — Spain CPI, interannual rate (December)
##    URL: https://ine.es/dynt3/inebase/es/index.htm?padre=1368&capsel=3466
##
##  CPI chain (base 2000 = 1.0), cumulative factor = Π(1 + ΔCPI_t):
##    ┌──────┬─────────────┬───────────┐
##    │ Year │  ΔCPI (Dec.)│  Factor   │
##    ├──────┼─────────────┼───────────┤
##    │ 2000 │ base        │ 1.000000  │
##    │ 2001 │  +4.0%      │ 1.040000  │
##    │ 2002 │  +2.7%      │ 1.068080  │
##    │ 2003 │  +4.0%      │ 1.110803  │
##    │ 2004 │  +2.6%      │ 1.139684  │
##    │ 2005 │  +3.2%      │ 1.176154  │
##    │ 2006 │  +3.7%      │ 1.219672  │
##    │ 2007 │  +2.7%      │ 1.252603  │
##    │ 2008 │  +4.2%      │ 1.305212  │
##    │ 2009 │  +1.4%      │ 1.323485  │
##    │ 2010 │  +0.8%      │ 1.334073  │
##    │ 2011 │  +3.0%      │ 1.374095  │
##    │ 2012 │  +2.4%      │ 1.407073  │
##    │ 2013 │  +2.9%      │ 1.447879  │
##    │ 2014 │  +0.3%      │ 1.452222  │
##    │ 2015 │  -1.0%      │ 1.437700  │
##    │ 2016 │   0.0%      │ 1.437700  │
##    │ 2017 │  +1.6%      │ 1.460703  │
##    │ 2018 │  +1.1%      │ 1.476771  │
##    │ 2019 │  +1.2%      │ 1.494492  │
##    │ 2020 │  +0.8%      │ 1.506448  │
##    │ 2021 │  -0.5%      │ 1.498916  │  ← n+2 payments of 2014-2020 only
##    └──────┴─────────────┴───────────┘
##
##
##  ══════════════════════════════════════════════════════════════════════════
##  PART III — DEFLATION TO € CONSTANT 2020 PRICES
##  ══════════════════════════════════════════════════════════════════════════
##
##  Formula:  Gasto_m_eur2020 = Gasto_m_eur00 × CPI_DEFLATOR_2000_2020
##
##  The deflator is computed by chaining the same CPI annual rates used in
##  the original conversion (methodological consistency):
##
##    CPI_DEFLATOR_2000_2020 = Π_{t=2001}^{2020} (1 + ΔCPI_t)
##                           = 1.50644811  (verified value)
##
##  This means 1 € of 2000 is equivalent to ~1.5064 € of 2020.
##
##  Source: INE — Spain CPI, interannual December rate.
##  URL: https://ine.es/dynt3/inebase/es/index.htm?padre=1368&capsel=3466
##
##  METHODOLOGICAL NOTE — CPI vs GDP deflator:
##  Since post-1999 data were originally deflated using the CPI (not the GDP
##  deflator), applying the same CPI in the reverse direction guarantees
##  consistency. Using the GDP deflator instead would underestimate values in
##  €2020 by approximately 15%.
