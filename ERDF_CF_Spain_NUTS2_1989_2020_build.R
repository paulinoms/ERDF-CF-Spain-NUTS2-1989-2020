# ── Scalar conversion function ────────────────────────────────────────────────
# Converts a single value from the original currency to thousands of constant
# 2000 euros. Implements both conversion routes (see Part II):
#   · "MEcus"  → always uses EUR/ECU (regardless of year)
#   · "MEuros" → uses EUR/ECU if year < 2000 (ECU-linked euros)
#              → uses CPI_factor if year >= 2000 (real current euros)
# Returns NA with a warning if the year is not in conv_master.
convert_to_eur00 <- function(value, year, unit, conv) {
  if (is.na(value) | is.na(year)) return(NA_real_)
  if (value == 0) return(0)
  f <- conv[conv$Year == year, ]
  if (nrow(f) == 0) { warning("No factor for year ", year); return(NA_real_) }
  f <- f[1, ]
  switch(as.character(unit),
         "MEcus"  = as.numeric(value * f$eur_per_ecu * 1000),
         "MEuros" = if (year < 2000) {
           as.numeric(value * f$eur_per_ecu  * 1000)
         } else {
           as.numeric(value * f$CPI_factor   * 1000)
         },
         { warning("Unknown unit: ", unit); NA_real_ }
  )
}

# ── Vectorised wrapper ────────────────────────────────────────────────────────
# Applies convert_to_eur00() row by row via mapply().
# IMPORTANT: 'u' must be a SCALAR constant for the entire batch of rows.
# If the unit varies per row (Cohesion Fund sheet), do NOT use this function;
# use mapply() directly with 'u' as a vector (see Section 7).
conv_vec <- function(v, y, u, conv) {
  as.numeric(mapply(convert_to_eur00, value = v, year = y,
                    MoreArgs = list(unit = u, conv = conv),
                    SIMPLIFY = TRUE))
}
